public class Carroll {
    public static void main(String[] args) {
        System.out.println("\"Begin at the");
        System.out.print("beginning,\"");
        System.out.println(", the King");
        System.out.print("said, very gravely, \"");
        System.out.println("and");
        System.out.println("go on till you come to");
        System.out.println("the end: then stop.\"");
        System.out.println(" \t\t  -Lewis Carroll");
    }
}
